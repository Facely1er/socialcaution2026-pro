import sgMail from '@sendgrid/mail';

export const handler = async (event, context) => {
  // Only allow POST
  if (event.httpMethod !== 'POST') {
    return {
      statusCode: 405,
      body: JSON.stringify({ error: 'Method not allowed' })
    };
  }

  try {
    const { email, report } = JSON.parse(event.body);

    // Validate inputs
    if (!email || !report) {
      return {
        statusCode: 400,
        body: JSON.stringify({ error: 'Missing email or report data' })
      };
    }

    // Initialize SendGrid with API key from environment variable
    const apiKey = process.env.SENDGRID_API_KEY;
    
    // FALLBACK: If SendGrid is not configured, return report data for client-side download
    if (!apiKey) {
      console.warn('SENDGRID_API_KEY not configured - returning report data for client-side download');
      return {
        statusCode: 200,
        headers: {
          'Content-Type': 'application/json',
          'X-Service-Status': 'fallback'
        },
        body: JSON.stringify({ 
          success: true, 
          message: 'Email service not configured. Please download the report instead.',
          fallback: true,
          report: report
        })
      };
    }

    sgMail.setApiKey(apiKey);

    // Build email content
    const htmlContent = `
      <!DOCTYPE html>
      <html>
      <head>
        <style>
          body { font-family: Arial, sans-serif; line-height: 1.6; color: #333; }
          .container { max-width: 600px; margin: 0 auto; padding: 20px; }
          .header { background: linear-gradient(135deg, #667eea 0%, #764ba2 100%); color: white; padding: 20px; border-radius: 8px 8px 0 0; }
          .content { background: #f9f9f9; padding: 20px; border-radius: 0 0 8px 8px; }
          .score-card { background: white; padding: 15px; margin: 10px 0; border-radius: 8px; border-left: 4px solid #667eea; }
          .score { font-size: 32px; font-weight: bold; color: #667eea; }
          .label { font-size: 14px; color: #666; text-transform: uppercase; }
          .narrative { background: white; padding: 15px; margin: 20px 0; border-radius: 8px; white-space: pre-wrap; }
          .footer { text-align: center; margin-top: 20px; font-size: 12px; color: #999; }
        </style>
      </head>
      <body>
        <div class="container">
          <div class="header">
            <h1>🛡️ Your Privacy Assessment Report</h1>
            <p>Generated on ${report.reportDate}</p>
          </div>
          <div class="content">
            <h2>Privacy Persona: ${report.persona.name}</h2>
            <p>${report.persona.description}</p>

            <div class="score-card">
              <div class="label">Privacy Exposure Score</div>
              <div class="score">${report.scores.exposureScore}/100</div>
            </div>

            <div class="score-card">
              <div class="label">Privacy Rights Score</div>
              <div class="score">${report.scores.rightsScore}/100</div>
            </div>

            <div class="score-card">
              <div class="label">Overall Privacy Health</div>
              <div class="score">${report.scores.overallPrivacyHealth}/100</div>
            </div>

            <h3>Summary & Recommendations</h3>
            <div class="narrative">${report.narrative}</div>

            <p style="margin-top: 30px;">
              <strong>Assessment Type:</strong> ${report.assessmentType}<br>
              <strong>Completed:</strong> ${new Date(report.completedAt).toLocaleDateString()}
            </p>
          </div>
          <div class="footer">
            <p>This report was generated by SocialCaution - Your Privacy Assessment Platform</p>
            <p>All data is processed locally and stored only on your device.</p>
          </div>
        </div>
      </body>
      </html>
    `;

    const textContent = `
SOCIALCAUTION PRIVACY ASSESSMENT REPORT
Generated: ${report.reportDate}

PERSONA: ${report.persona.name}
${report.persona.description}

SCORES:
- Privacy Exposure Score: ${report.scores.exposureScore}/100
- Privacy Rights Score: ${report.scores.rightsScore}/100
- Overall Privacy Health: ${report.scores.overallPrivacyHealth}/100

SUMMARY & RECOMMENDATIONS:
${report.narrative}

Assessment Type: ${report.assessmentType}
Completed: ${new Date(report.completedAt).toLocaleDateString()}

---
This report was generated by SocialCaution - Your Privacy Assessment Platform
All data is processed locally and stored only on your device.
    `;

    // Send email with retry logic
    let sendAttempts = 0;
    const maxAttempts = 2;
    let lastError = null;

    while (sendAttempts < maxAttempts) {
      try {
        const msg = {
          to: email,
          from: process.env.SENDGRID_FROM_EMAIL || 'noreply@ermits.com',
          subject: `Your SocialCaution Privacy Report - ${report.reportDate}`,
          text: textContent,
          html: htmlContent,
          attachments: [
            {
              content: Buffer.from(JSON.stringify(report, null, 2)).toString('base64'),
              filename: `privacy-report-${report.timestamp?.split('T')[0] || new Date().toISOString().split('T')[0]}.json`,
              type: 'application/json',
              disposition: 'attachment'
            }
          ]
        };

        await sgMail.send(msg);
        
        return {
          statusCode: 200,
          body: JSON.stringify({ success: true, message: 'Email sent successfully' })
        };
      } catch (sendError) {
        sendAttempts++;
        lastError = sendError;
        
        // Check if it's a rate limit or temporary error
        const isRetryable = sendError.code === 429 || 
                          (sendError.response && sendError.response.status >= 500);
        
        if (sendAttempts < maxAttempts && isRetryable) {
          // Wait before retry (exponential backoff)
          await new Promise(resolve => setTimeout(resolve, 1000 * sendAttempts));
          continue;
        }
        
        // If not retryable or max attempts reached, fall through to fallback
        break;
      }
    }

    // FALLBACK: If SendGrid fails, return report data for client-side download
    console.error('SendGrid email failed after retries:', lastError);
    return {
      statusCode: 200,
      headers: {
        'Content-Type': 'application/json',
        'X-Service-Status': 'fallback'
      },
      body: JSON.stringify({ 
        success: true, 
        message: 'Email service temporarily unavailable. Please download the report instead.',
        fallback: true,
        report: report
      })
    };

  } catch (error) {
    console.error('Error sending email:', error);
    return {
      statusCode: 500,
      body: JSON.stringify({ error: 'Failed to send email', details: error.message })
    };
  }
};
